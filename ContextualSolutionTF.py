total_reward = np.zeros([cMaze.num_states, cMaze.num_actions])
e = 0.1     # random action 

init = tf.global_variables_initializer()


with tf.Session() as sess:
    sess.run(init)
    i = 0

    while i < total_episodes:
        s = cMaze.getState()

        if np.random.rand(1) < e:
            action = np.random.randint(cMaze.num_actions)
        else:
            action = sess.run(myAgent.chosen_action, feed_dict={myAgent.state_in:[s]})

        reward = cMaze.move(action)
        
        feed_dict = { myAgent.reward_holder: [reward], myAgent.action_holder: [action], myAgent.state_in: [s]  }
        
        _, ww = sess.run([myAgent.update, weights], feed_dict = feed_dict)

        total_reward[s, action] += reward

        if i % 500 == 0:
            print ("Mean reward for each of the " + str(cMaze.num_states) + " bandits: " + str(np.mean(total_reward, axis=1)))
        
        i += 1


