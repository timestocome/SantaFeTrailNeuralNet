# create maze
class contextual_maze():

    def __init__(self):

        self.state = 0
        self.states = new_maze
        self.num_states = len(new_maze)
        self.num_actions = 4

    

    def getState(self):
        return self.state


    def move(self, action):

        state = self.state

         # take a step if we can with out falling off edge of our world
        if action == 0:         # up
            if state > 31:                                  self.state = state - 32
        elif action == 1:       # right
            if ((state + 1) % 32 != 0 and state < 1023):    self.state = state + 1 
        elif action == 2:       # down 
            if state < (1023-31):                           self.state = state + 32
        elif action == 3:       # left
            if (state % 32 != 0 and state > 0):             self.state = state - 1
        


        result = self.states[self.state]  # collect reward
        self.states[self.state] = 0        # remove reward from maze

        if result == 1: return 1 
        else: return 0



class agent():

    def __init__(self, lr, s_size, a_size):

        self.state_in = tf.placeholder(shape=[1], dtype=tf.int32)
        state_in_OH = slim.one_hot_encoding(self.state_in, s_size)
        output = slim.fully_connected(state_in_OH, 
                                        a_size, 
                                        biases_initializer=None, 
                                        activation_fn=tf.nn.sigmoid,
                                        weights_initializer=tf.ones_initializer())
        self.output = tf.reshape(output, [-1])
        self.chosen_action = tf.argmax(self.output, 0)
        self.reward_holder = tf.placeholder(shape=[1], dtype=tf.float32)
        self.action_holder = tf.placeholder(shape=[1], dtype=tf.int32)
        self.responsible_weight = tf.slice(self.output, self.action_holder, [1])
        self.loss = -(tf.log(self.responsible_weight) * self.reward_holder)
        optimizer = tf.train.GradientDescentOptimizer(learning_rate=lr)
        self.update = optimizer.minimize(self.loss)



tf.reset_default_graph()

