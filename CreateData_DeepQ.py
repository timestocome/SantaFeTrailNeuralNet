    reward = -1
    for i in range(512):        # amount of wandering per game

        # initalize game board
        if i == 0:
            reward_maze = copy.copy(maze)
            state = 0
            new_state = 0
            reward = 1
            reward_maze[0] = 2        # mark player location

        # take random action
        action = random_action()

        if action == 0:         # up
                if state > 31:  new_state = state - 32
        elif action == 1:       # right
                if ((state + 1) % 32 != 0 and state < 1023):  new_state = state + 1 
        elif action == 2:       # down 
                if state < (1023-31):  new_state = state + 32
        elif action == 3:       # left
                if (state % 32 != 0 and state > 0): new_state = state - 1
        
        if new_state == None: new_state = state     # impossible move
        reward = reward_maze[new_state]             # see if food pellet still there


        # map reward, if any to action vector ( 4 zeros, 1 in the action we took if there is a reward there )
        action_rewards = np.zeros(number_of_actions)
        if new_state != state:      # make sure we moved so we don't count player mark as a reward
            action_rewards[action] = reward


